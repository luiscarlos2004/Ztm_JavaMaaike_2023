package FMethodsExercices;

public class AdvancedCalculator {
    public static void performCalculation(int n1, int n2){
        int sum = n1 + n2;
        int difference = n1 - n2;
        int product = n1 * n2;
        int quotient = n1 / n2;
        System.out.println("Sum: " + sum + "Product: " + product + "Quotient: " + quotient + "Difference" + difference);
    }
}
